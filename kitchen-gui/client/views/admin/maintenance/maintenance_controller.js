this.AdminMaintenanceController = RouteController.extend({
	template: "Admin",

	yieldTemplates: {
		'AdminMaintenance': { to: 'AdminSubcontent'},
	},

	onBeforeAction: function() {
		this.next();
	},

	action: function() {
		if(this.isReady()) { this.render(); } else { this.render("Admin"); this.render("loading", { to: "AdminSubcontent" });}
	},

	isReady: function() {
		

		var subs = [
		];
		var ready = true;
		_.each(subs, function(sub) {
			if(!sub.ready())
				ready = false;
		});
		return ready;
	},

	data: function() {
		

		return {
			params: this.params || {}
		};
	},

	onAfterAction: function() {
		
	}
});
